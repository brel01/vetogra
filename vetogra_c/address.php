$("#roller").hide();
$("#add_error_box").hide();


 function submit(){
   $("#login").hide();
   $("#roller").show();
   document.getElementById("add_error").innerHTML = "";
   $("#add_error_box").hide();
   var address = $("#address").val();
   if(address.length > 0){
     if(address.length >= 40){
       document.getElementById("add_error").innerHTML = "";
       $("#add_error_box").hide();
       $.ajax({
         type: 'POST',
         url: '../submit_eth_address.php',
         data: {eth_add:address},
         dataType: 'json',
         success: function(response){
           if(response == "success"){
             $("#roller").hide();
             $("#login").show();
             window.location.replace("../dashboard");
           }else{
             $("#roller").hide();
             $("#login").show();
             document.getElementById("add_error").innerHTML = response;
             $("#add_error_box").show();
             setTimeout(function(){ $("#add_error_box").hide();; }, 3000);
           }
         }
       })
     }else{
       $("#roller").hide();
       $("#login").show();
       var error = "Invaid ETH Address";
       document.getElementById("add_error").innerHTML = error;
       $("#add_error_box").show();
       setTimeout(function(){ $("#add_error_box").hide();; }, 3000);
     }
   }
 }
